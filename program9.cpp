// if - else if - else (C++ programming)
#include <iostream>
using namespace std;

int main ()
{
  int X{20};
  int Y = 30;

  if (X == Y)
  {
    cout << "X is equal to Y" << endl;
  }
  else if (X > Y)
  {
    cout << "X is greater than Y" << endl;
  }
  else
  {
    cout << "X is less than Y" << endl;
  }


  cout << "Example of If - Else If - Else Statement in C++" << endl;
}
